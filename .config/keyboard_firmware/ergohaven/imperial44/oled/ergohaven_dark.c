void ergohaven_dark_draw(void) {
  uint16_t timer = timer_elapsed(ergohaven_dark_timer);

  if (timer < 150) {
    show_text ? ergohaven_dark_text_glitch_dirty() : ergohaven_dark_logo_glitch_dirty();

    return;
  }

  if (timer < 250) {
    show_text ? ergohaven_dark_text_glitch(true) : ergohaven_dark_logo_glitch(true);

    return;
  }

  if (timer > 9750 && timer < 9850) {
    show_text ? ergohaven_dark_text_glitch(true) : ergohaven_dark_logo_glitch(true);

    return;
  }

  if (timer > 9850 && timer < 10000) {
    show_text ? ergohaven_dark_text_glitch_dirty() : ergohaven_dark_logo_glitch_dirty();

    return;
  }

  if (timer > 10000) {
    show_text = !show_text;
    ergohaven_dark_timer = timer_read();
  }

  if (glitch && 0 != frame_count) {
    frame_count--;
    show_text ? ergohaven_dark_text_glitch(true) : ergohaven_dark_logo_glitch(true);

    return;
  }

  glitch = false;
  dirty = false;

  show_text ? ergohaven_dark_text_clean() : ergohaven_dark_logo_clean();

  if (1 == rand() % 60) {
    glitch = true;
    frame_count = 1 + rand() % 4;

    return;
  }

  if (1 == rand() % 60) {
    glitch = true;
    frame_count = 1 + rand() % 10;
    dirty = frame_count > 5;
  }
}

